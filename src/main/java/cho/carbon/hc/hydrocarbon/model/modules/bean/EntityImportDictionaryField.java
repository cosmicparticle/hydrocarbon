package cho.carbon.hc.hydrocarbon.model.modules.bean;

public class EntityImportDictionaryField {
	private Integer id;
	private String name;
	private String pattern;
	private String type;
	private Integer casLevel;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPattern() {
		return pattern;
	}
	public void setPattern(String pattern) {
		this.pattern = pattern;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Integer getCasLevel() {
		return casLevel;
	}
	public void setCasLevel(Integer casLevel) {
		this.casLevel = casLevel;
	}
}
